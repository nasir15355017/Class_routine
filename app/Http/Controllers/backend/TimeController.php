     */
    public function store(Request $request)
    {
        $time= new Class_time_slot();
        $time->start_time = $request->input('start_time');
        $time->end_time = $request->input('end_time');
        $time->save();
        session()->flash('message','Created Successfully');
        return redirect()->back();
    }

